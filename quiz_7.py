def 크리스마스트리_출력(높이):
    if 높이 < 1:
        print("유효하지 않은 높이입니다. 양수를 입력하세요.")
    else:
        for i in range(1, 높이 + 1):
            공백 = ' ' * (높이 - i + 2)  # 공백 조절
            별 = '*' * (2 * i - 1)        # 별 조절
            print(공백 + 별)

try:
    높이 = int(input("크리스마스 트리의 높이를 설정하세요: "))
    크리스마스트리_출력(높이)
except ValueError:
    print("올바른 숫자를 입력하세요.")
